    class Err {

    //=====================================================================================================================||
    //===|| Crucial
    //=====================================================================================================================||

        static function critical($message, $private = '') {
            echo($message);
            echo('<br><i>' . $private . '</i>');
            exit();
        }

    //=====================================================================================================================||
    //===|| Crucial
    //=====================================================================================================================||

        static function db($message, $private = '') {
            echo('DBERROR: ' . $message);
            echo('<br><i>' . $private . '</i>');
            exit();
        }


        //=====================================================================================================================||
        //===|| Marker
        //=====================================================================================================================||

        static function marker($marker, $type, $lang) {
            echo('<br>LANGERROR: ' . $marker . ' of type['.$type.'] / lang['.$lang.'] not found');
        }


    }
