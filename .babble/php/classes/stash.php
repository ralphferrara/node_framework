    global $__stash;
    $__stash = array();

    //=====================================================================================================================||
    //===|| Class
    //=====================================================================================================================||

    class Stash {

        //=====================================================================================================================||
        //===|| Private Access
        //=====================================================================================================================||

        static function env($area, $name = null, $variable = null) {
            global $__stash;
            $hash = strtoupper($name);
            if ($variable === null && $name == null) {
                if (isset($__stash[$area])) return $__stash[$area]; else return Err::critical('Stash::Area not configured', 'Stash::Access::Area not configured - ' . $area);
            }
            if ($variable === null) return (!isset($__stash[$area][$hash])) ? null : $__stash[$area][$hash];
            $__stash[$area][$hash] = $variable;
            return $variable;
        }

        //=====================================================================================================================||
        //===|| Class
        //=====================================================================================================================||

    }