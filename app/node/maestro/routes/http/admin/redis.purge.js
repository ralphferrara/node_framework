    function RedisPurge(myTask) {
        console.log('Purge Redis')
    };

    /*||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||
    //|| Process
    //||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||*/

    RedisPurge.prototype.handler = function(myTask) {
        app.module("Redis").flush(function(myStatus) {
            if (myStatus === true) return myTask.success({}, app.lang("auth.success.general", myTask.lang, "errors"));
            return myTask.fail({}, myStatus);
        })
    };


